    public void loadCity() {

        try {

            ResultSet rs = DB.DB.search("SELECT `city` FROM city;");
            Vector<String> v = new Vector<>();

            while (rs.next()) {
                v.add(rs.getString("city"));
            }

            DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);

            jComboBox1.setModel(dcm);

        } catch (Exception e) {
            System.out.println(e);
        }

    }

    private void tableLoad() {

        TableModel model = jTable1.getModel();
        DefaultTableModel dtm = (DefaultTableModel) model;
        dtm.setRowCount(0);

        try {
            ResultSet rs = DB.DB.search("SELECT * FROM customer NATURAL JOIN city WHERE customer.city_city_id = city.city_id;");

            while (rs.next()) {
                Vector<String> v = new Vector<>();

                v.add(rs.getString("mobile"));
                v.add(rs.getString("name"));
                v.add(rs.getString("city"));
                v.add(rs.getString("date_registerd"));

                dtm.addRow(v);

            }
        } catch (Exception e) {
        }

    }

