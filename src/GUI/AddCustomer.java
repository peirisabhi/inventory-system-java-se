    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        String mobile = jTextField1.getText();
        String name = jTextField2.getText();
        String city = jComboBox1.getSelectedItem().toString();

        try {
            ResultSet rs = DB.DB.search("SELECT city_id FROM city WHERE city = '" + city + "';");

            if (rs.next()) {
                String city_id = rs.getString("city_id");

                Date d = new Date();
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                String date = sdf.format(d);

                DB.DB.iud("INSERT INTO customer(mobile,name,city_city_id,date_registerd)"
                        + " VALUES('" + mobile + "','" + name + "','" + city_id + "','" + date + "')");

                JOptionPane.showMessageDialog(this, "Customer Added", "Success", JOptionPane.INFORMATION_MESSAGE);

                tableLoad();

            }
        } catch (Exception e) {
            System.out.println(e);
        }


    }//GEN-LAST:event_jButton4ActionPerformed

