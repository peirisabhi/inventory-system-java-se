    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String mobile = jTextField2.getText();
        String paid_amount = jTextField5.getText();


        DB.DB.iud("INSERT INTO `inventory_system`.`grn` (`supplier_mobile`, `date_time`, `paid_amount`) VALUES ('" + mobile + "', NOW(), '" + paid_amount + "');");

        int rowCount = jTable1.getRowCount();

        for (int i = 0; i < rowCount; i++) {
            String id = jTable1.getValueAt(i, 0).toString();
            String qty = jTable1.getValueAt(i, 5).toString();
            String buying_price = jTable1.getValueAt(i, 4).toString();

            try {
                ResultSet rs = DB.DB.search("SELECT qty FROM product_stock WHERE id = '" + id + "';");

                if (rs.next()) {
                    String avilable = rs.getString("qty");
                    int avilable1 = Integer.parseInt(avilable);

                    int now = Integer.valueOf(qty);
                    int newTot = avilable1 + now;

                    DB.DB.iud("UPDATE product_stock SET qty = '" + newTot + "' WHERE id = '" + id + "'");
                }
            } catch (Exception e) {
                System.out.println(e);
            }

            try {
                ResultSet rss = DB.DB.search("SELECT MAX(id) FROM grn;");

                if (rss.next()) {
                    String grnId = rss.getString(1);

                    DB.DB.iud("INSERT INTO `inventory_system`.`grn_product` (`grn_id`, `product_stock_id`, `qty`, `buying_price`) VALUES ('" + grnId + "', '" + id + "', '" + qty + "', '" + buying_price + "');");
                }

            } catch (Exception e) {
                System.out.println(e);
            }

        }
        
        JOptionPane.showMessageDialog(this, "Success", "Success", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_jButton2ActionPerformed

