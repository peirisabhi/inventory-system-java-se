    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        if (evt.getClickCount() >= 2) {
            int row = jTable1.getSelectedRow();
            TableModel model = jTable1.getModel();
            DefaultTableModel dtm = (DefaultTableModel) model;

            double price = Double.parseDouble(jTable1.getValueAt(row, 4).toString());
            double qty = Double.parseDouble(jTable1.getValueAt(row, 5).toString());

            tot = tot - price * qty;

            jLabel19.setText(String.valueOf(tot));

            dtm.removeRow(row);
        }

    }//GEN-LAST:event_jTable1MouseClicked

